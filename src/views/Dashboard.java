    private void hapusData(JTable table, String tbName){
        if (!table.getSelectionModel().isSelectionEmpty()){
            int getRow = table.getSelectedRow();
            int modelRow = table.convertRowIndexToModel(getRow);
            String id = unformatKode(table.getModel().getValueAt(modelRow, 1).toString());
            
            int confirm = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data?", "Konfirmasi hapus data",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION){
                conn = Koneksi.getKoneksi();
                try{
                    query = "DELETE FROM "+tbName+" WHERE id="+id;
                    conn = Koneksi.getKoneksi();
                    stmt = conn.prepareStatement(query);
                    if (stmt.executeUpdate(query) > 0){
                        JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
                        getDataPanel(table);
                    }else{
                        JOptionPane.showMessageDialog(null, "Data gagal dihapus!");
                    }
                }catch (SQLException e){
                    System.out.println(e.getMessage());
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Pilih baris data yang ingin dihapus terlebih dulu!");
        }
    }
    
