    private void btnCetakLapPresensiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCetakLapPresensiMouseClicked
        if (!tabelLapPresensi.getSelectionModel().isSelectionEmpty()){
            int getRow = tabelLapPresensi.getSelectedRow();
            int modelRow = tabelLapPresensi.convertRowIndexToModel(getRow);
            String id = tabelLapPresensi.getModel().getValueAt(modelRow, 1).toString();
            
            int confirm1 = JOptionPane.showConfirmDialog(null, 
                "Laporan baru akan otomatis dibuat berdasarkan data saat ini, lanjutkan?", 
                "Konfirmasi Cetak Laporan", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if (confirm1 == JOptionPane.YES_OPTION){
                try{
                    conn = Koneksi.getKoneksi();
                    stmt = conn.createStatement();
                    try{                          
                        String report = "/reports/LapPresensi.jasper";
                        InputStream path = getClass().getResourceAsStream(report);
                        
                        HashMap param = new HashMap();

                        param.put("id_krw", id);

//                        File file = new File(report);
//                        JasperReport jrept = (JasperReport)JRLoader.loadObject(file);
                        JasperPrint jprint = JasperFillManager.fillReport(path, param, conn);
                        JasperViewer.viewReport(jprint, false);
                        JasperViewer.setDefaultLookAndFeelDecorated(true);
                    }catch (Exception e){
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }catch (SQLException e){
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Pilih baris data terlebih dahulu!");
        }
    }//GEN-LAST:event_btnCetakLapPresensiMouseClicked

