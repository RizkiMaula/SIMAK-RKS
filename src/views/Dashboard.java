    private static String formatRupiah(int value) {
        String result;

        if (value < 1000) {
            result = Integer.toString(value);
        } else if (value < 1000000) {
            result = Integer.toString(value / 1000) + " rb";
        } else if (value < 1000000000) {
            result = Integer.toString(value / 1000000) + " jt";
        } else {
            result = Integer.toString(value / 1000000000) + " M";
        }
        return result;
    }
    
    private static void tampilDataOverview(){
        String queries[] = {
            "SELECT COUNT(*) AS data FROM penjualan",
            "SELECT SUM(tagihan) AS data FROM penjualan",
            "SELECT SUM(jumlah) AS data FROM pengeluaran"
        };
        JLabel labels[] = {dataTotalPenjualan, dataTotalPendapatan, dataTotalPengeluaran};
        
        conn = Koneksi.getKoneksi();
        for (int i=0; i<labels.length; i++){
            try{
                stmt = conn.createStatement();
                res = stmt.executeQuery(queries[i]);
                if (res.next()){
                    
                    labels[i].setText(formatRupiah(Integer.parseInt(res.getString("data"))));
                }
            }catch (SQLException e){
                System.out.println(e.getMessage());
            }
        }
    }
    
